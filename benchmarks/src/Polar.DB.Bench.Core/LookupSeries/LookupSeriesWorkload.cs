using System;
using System.Collections.Generic;
using System.Globalization;
using Polar.DB.Bench.Core.Models;

namespace Polar.DB.Bench.Core.LookupSeries;

/// <summary>
/// Shared contract for lookup-series workloads executed by the current Polar.DB adapter and SQLite adapter.
/// It intentionally allows only stable ready-file lookup keys: int, long and Guid.
/// </summary>
public static class LookupSeriesWorkload
{
    public const string ExactOneWorkloadKey = "lookup-exact-one";
    public const string AllMatchingWorkloadKey = "lookup-all-matching";

    public const string KeyKindOption = "keyKind";
    public const string DuplicateGroupSizeOption = "duplicateGroupSize";
    public const string ReopenAfterBuildOption = "reopenAfterBuild";

    public const int DefaultLookupCount = 10_000;
    public const int DefaultDuplicateGroupSize = 10;
    public const int CommonLookupSeedSalt = unchecked((int)0x613b4d5f);

    public static bool IsLookupSeries(string? workloadKey)
    {
        return string.Equals(workloadKey, ExactOneWorkloadKey, StringComparison.OrdinalIgnoreCase) ||
               string.Equals(workloadKey, AllMatchingWorkloadKey, StringComparison.OrdinalIgnoreCase);
    }

    public static LookupSeriesOptions Resolve(ExperimentSpec spec)
    {
        if (!IsLookupSeries(spec.Workload.WorkloadKey))
        {
            throw new NotSupportedException(
                $"Workload '{spec.Workload.WorkloadKey}' is not a lookup-series workload.");
        }

        var mode = string.Equals(spec.Workload.WorkloadKey, ExactOneWorkloadKey, StringComparison.OrdinalIgnoreCase)
            ? LookupSeriesMode.ExactOne
            : LookupSeriesMode.AllMatching;

        var keyKindText = GetOption(spec.Workload, KeyKindOption, "int");
        var keyKind = keyKindText.Trim().ToLowerInvariant() switch
        {
            "int" or "int32" => LookupKeyKind.Int32,
            "long" or "int64" => LookupKeyKind.Int64,
            "guid" or "uuid" => LookupKeyKind.Guid,
            _ => throw new NotSupportedException(
                $"Lookup key kind '{keyKindText}' is not supported. Supported values: int, long, guid.")
        };

        var lookupCount = spec.Workload.LookupCount.GetValueOrDefault(DefaultLookupCount);
        if (lookupCount <= 0)
        {
            throw new InvalidOperationException("workload.lookup must be >= 1 for lookup-series workloads.");
        }

        if (spec.Dataset.RecordCount <= 0)
        {
            throw new InvalidOperationException("dataset.count must be >= 1 for lookup-series workloads.");
        }

        if (spec.Dataset.RecordCount > int.MaxValue)
        {
            throw new InvalidOperationException(
                "lookup-series currently supports dataset.count <= int.MaxValue because probes are generated by Random.Next.");
        }

        var duplicateGroupSize = ResolvePositiveIntOption(
            spec.Workload,
            DuplicateGroupSizeOption,
            DefaultDuplicateGroupSize);

        if (mode == LookupSeriesMode.ExactOne)
        {
            duplicateGroupSize = 1;
        }

        var reopenAfterBuild = ResolveBooleanOption(spec.Workload, ReopenAfterBuildOption, fallback: true);

        return new LookupSeriesOptions(
            mode,
            keyKind,
            lookupCount,
            duplicateGroupSize,
            reopenAfterBuild);
    }

    public static IComparable CreateKey(LookupKeyKind keyKind, LookupSeriesMode mode, int seed, int recordOrdinal, int duplicateGroupSize)
    {
        if (recordOrdinal <= 0)
        {
            throw new ArgumentOutOfRangeException(nameof(recordOrdinal));
        }

        var keyOrdinal = mode == LookupSeriesMode.ExactOne
            ? recordOrdinal
            : ((recordOrdinal - 1) / duplicateGroupSize) + 1;

        return keyKind switch
        {
            LookupKeyKind.Int32 => keyOrdinal,
            LookupKeyKind.Int64 => 10_000_000_000L + keyOrdinal,
            LookupKeyKind.Guid => CreateDeterministicGuid(seed, keyOrdinal),
            _ => throw new ArgumentOutOfRangeException(nameof(keyKind))
        };
    }

    public static LookupProbe CreateRandomProbe(
        LookupKeyKind keyKind,
        LookupSeriesMode mode,
        int seed,
        long recordCount,
        int duplicateGroupSize,
        Random random)
    {
        var intRecordCount = checked((int)recordCount);
        if (mode == LookupSeriesMode.ExactOne)
        {
            var ordinal = random.Next(1, intRecordCount + 1);
            return new LookupProbe(
                CreateKey(keyKind, mode, seed, ordinal, duplicateGroupSize),
                1,
                ordinal);
        }

        var groupCount = (int)Math.Ceiling(recordCount / (double)duplicateGroupSize);
        var groupOrdinal = random.Next(1, groupCount + 1);
        var firstRecordOrdinal = ((groupOrdinal - 1) * duplicateGroupSize) + 1;
        var remaining = intRecordCount - firstRecordOrdinal + 1;
        var expected = Math.Min(duplicateGroupSize, remaining);

        return new LookupProbe(
            CreateKey(keyKind, mode, seed, firstRecordOrdinal, duplicateGroupSize),
            expected,
            firstRecordOrdinal);
    }

    public static int HashSupportedKey(IComparable key)
    {
        return key switch
        {
            int value => value,
            long value => unchecked((int)(value ^ (value >> 32))),
            Guid value => StableGuidHash(value),
            _ => throw new NotSupportedException(
                $"Lookup key type '{key.GetType().FullName}' is not supported. Supported key types: int, long, Guid.")
        };
    }

    public static Guid CreateDeterministicGuid(int seed, int ordinal)
    {
        Span<byte> bytes = stackalloc byte[16];
        BitConverter.TryWriteBytes(bytes[..4], seed);
        BitConverter.TryWriteBytes(bytes.Slice(4, 4), ordinal);
        BitConverter.TryWriteBytes(bytes.Slice(8, 4), unchecked(seed * 397) ^ ordinal);
        BitConverter.TryWriteBytes(bytes.Slice(12, 4), unchecked(seed + ordinal * 7919));
        return new Guid(bytes);
    }

    public static int StableGuidHash(Guid value)
    {
        Span<byte> bytes = stackalloc byte[16];
        value.TryWriteBytes(bytes);
        var a = BitConverter.ToInt32(bytes[..4]);
        var b = BitConverter.ToInt32(bytes.Slice(4, 4));
        var c = BitConverter.ToInt32(bytes.Slice(8, 4));
        var d = BitConverter.ToInt32(bytes.Slice(12, 4));
        return a ^ b ^ c ^ d;
    }

    public static bool ResolveBooleanOption(WorkloadSpec workload, string key, bool fallback)
    {
        var value = GetOption(workload, key, fallback ? "true" : "false");
        return value.Trim().ToLowerInvariant() switch
        {
            "1" or "true" or "yes" or "y" => true,
            "0" or "false" or "no" or "n" => false,
            _ => fallback
        };
    }

    public static int ResolvePositiveIntOption(WorkloadSpec workload, string key, int fallback)
    {
        var value = GetOption(workload, key, fallback.ToString(CultureInfo.InvariantCulture));
        return int.TryParse(value, NumberStyles.Integer, CultureInfo.InvariantCulture, out var parsed) && parsed > 0
            ? parsed
            : fallback;
    }

    public static string GetOption(WorkloadSpec workload, string key, string fallback)
    {
        return workload.Parameters is not null && workload.Parameters.TryGetValue(key, out var value) && !string.IsNullOrWhiteSpace(value)
            ? value
            : fallback;
    }
}

public enum LookupSeriesMode
{
    ExactOne,
    AllMatching
}

public enum LookupKeyKind
{
    Int32,
    Int64,
    Guid
}

public sealed record LookupSeriesOptions(
    LookupSeriesMode Mode,
    LookupKeyKind KeyKind,
    int LookupCount,
    int DuplicateGroupSize,
    bool ReopenAfterBuild);

public sealed record LookupProbe(IComparable Key, int ExpectedCount, int FirstRecordOrdinal);
